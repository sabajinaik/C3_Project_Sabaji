public class Item {
    private String name;
    private int price;

    public Item(String name, int price) {
        this.name = name;
        this.price = price;
    }

    public String getName() {
        return name;
    }
    @Override
    public String toString(){
        return  name + ":"
                + price
                + "\n"
                ;
    }
    //<Sabaji><04/04/2021><Part 3: Solution>
    public int getPrice(){
        return this.price;
    }
}
